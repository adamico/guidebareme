      <p>D'évaluation toujours très difficile en raison de l'intervention de facteurs extrinsèques. À n'évaluer qu'après de multiples épreuves de contrôle avec, si nécessaire, mise en œuvre de nouvelles techniques électrophysiologiques.</p>
